// üöÄ Inicializaci√≥n principal SIMPLE
async function init() {
  try {
    const site = getCurrentSite();
    const url = window.location.href;

    console.log(`\nüîÑ INIT: ${site} en ${url}`);

    // Solo ejecutar en sitios espec√≠ficos
    if (!site) {
      console.log('‚ùå Sitio no compatible');
      return;
    }

    // Cargar configuraciones
    await loadSettings();
    console.log('‚öôÔ∏è Configuraciones:', currentSettings);

    // Aplicar configuraciones seg√∫n el sitio
    switch (site) {
      case 'youtube':
        handleYouTube();
        break;
      case 'instagram':
        handleInstagram();
        break;
      case 'x':
        handleX();
        break;
      default:
        console.log('‚ùå Sitio no soportado');
    }

    console.log('‚úÖ Init completado\n');
  } catch (error) {
    console.log('‚ùå Error en init:', error);
  }
}

// Funci√≥n para detectar cambios de URL (solo eventos del navegador)
function detectPageChanges() {
  try {
    let currentUrl = window.location.href;
    const currentSite = getCurrentSite();

    console.log(`üîß SETUP: Detectando cambios en ${currentSite} desde ${currentUrl}`);

    // ‚ú® Funci√≥n SIMPLE para determinar si necesitamos reiniciar
    function needsRestart(oldUrl, newUrl) {
      try {
        const oldPath = new URL(oldUrl).pathname;
        const newPath = new URL(newUrl).pathname;

        // Solo reiniciar si cambi√≥ la secci√≥n principal
        const oldSection = oldPath.split('/')[1] || 'home';
        const newSection = newPath.split('/')[1] || 'home';

        const shouldRestart = oldSection !== newSection;

        if (shouldRestart) {
          console.log(`üîÑ Cambio de secci√≥n: ${oldSection} ‚Üí ${newSection}`);
        } else {
          console.log(`‚û°Ô∏è Navegaci√≥n interna: ${oldPath} ‚Üí ${newPath}`);
        }

        return shouldRestart;
      } catch (error) {
        console.log('‚ùå Error analizando URL:', error);
        return true;
      }
    }

    // üîç Funci√≥n para verificar cambios de URL
    function checkUrlChange() {
      const newUrl = window.location.href;
      if (currentUrl !== newUrl) {
        console.log(`üîç URL CAMBI√ì: "${currentUrl}" ‚Üí "${newUrl}"`);

        if (needsRestart(currentUrl, newUrl)) {
          console.log(`‚ö° REINICIANDO...`);
          currentUrl = newUrl;
          setTimeout(() => {
            init().catch(error => console.log('‚ùå Error reiniciando:', error));
          }, 200);
        } else {
          console.log(`‚û°Ô∏è Solo actualizando URL interna`);
          currentUrl = newUrl;
        }
      }
    }

    // üéØ Escuchar eventos de navegaci√≥n
    window.addEventListener('popstate', () => {
      console.log('üîô POPSTATE detectado');
      setTimeout(checkUrlChange, 50);
    });

    // üéØ Interceptar pushState/replaceState para SPAs
    const originalPushState = history.pushState;
    const originalReplaceState = history.replaceState;

    history.pushState = function (...args) {
      console.log('üì§ PUSH STATE:', args[2] || 'sin URL');
      originalPushState.apply(history, args);
      setTimeout(checkUrlChange, 50);
    };

    history.replaceState = function (...args) {
      console.log('üîÑ REPLACE STATE:', args[2] || 'sin URL');
      originalReplaceState.apply(history, args);
      setTimeout(checkUrlChange, 50);
    };

    // üö® DEBUGGING: Verificaci√≥n adicional cada 2 segundos (TEMPORAL)
    setInterval(() => {
      checkUrlChange();
    }, 2000);

  } catch (error) {
    console.log('Error configurando detecci√≥n de cambios de p√°gina:', error);
  }
}

// Ejecutar cuando el DOM est√© listo
